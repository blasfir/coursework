# циклічний зсув вправо 64-бітного числа


def rotate_right_8(value):
    value = value & 0xFFFFFFFFFFFFFFFF
    rotated = ((value >> 8) | (value << 56)
               ) & 0xFFFFFFFFFFFFFFFF
    return rotated

# циклічний зсув вліво 64-бітного числа


def rotate_left_8(value):
    value = value & 0xFFFFFFFFFFFFFFFF
    rotated = ((value << 8) | (value >> 56)
               ) & 0xFFFFFFFFFFFFFFFF
    return rotated

